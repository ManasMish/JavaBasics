package oops_class13_21_01_2024;

import java.util.Stack;

public class lab188 {
    public static void main(String[] args) {
        Stack<String> s1=new Stack<>();
        s1.push("Manas");
        s1.push("Kumar");
        s1.push("Mishra");
        System.out.println(s1);
        System.out.println(s1.pop());
        System.out.println(s1);
        System.out.println(s1.peek());
        System.out.println(s1);
        System.out.println(s1.pop());
        System.out.println(s1);
    }
}
